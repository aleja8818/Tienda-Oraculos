let fila=document.getElementById("fila")


//RECORRIENDO EL ARREGLO
productos.forEach(function(producto){
    
    //creando un div desde js
    let columna=document.createElement("div")
    //adiciona la columna
    columna.classList.add("col")

    //creando una tarjeta
    let tarjeta=document.createElement("div")
    //adiciona la clase
    tarjeta.classList.add("card","shadow","text-center","h-100","p-5")

    //creando una imagen
    let imagen=document.createElement("img")
    imagen.classList.add("img-fluid","w-75","d-block","mx-auto")
    //adiciona las fotos de la variable auxiliar del arreglo, accede porque son dos fotos en un array dentro de un objeto
    imagen.src=producto.fotos[0]
    //detectando evento de mouse
    imagen.addEventListener("mouseover", function(){
        imagen.src=producto.fotos[1]
    })
    imagen.addEventListener("mouseleave", function(){
        imagen.src=producto.fotos[0]
    })

    //creando un nombre
    let nombre=document.createElement("h3")
    nombre.classList.add("text-dark")
    nombre.textContent=producto.nombre

    //creando precio
    let precio=document.createElement("h2")
    precio.classList.add("text-success")
    precio.textContent="$"+producto.precio

    //creando la descripcion
    let descripcion=document.createElement("p")
    descripcion.classList.add("text-danger","d-none")
    descripcion.textContent=producto.descripcion

    //creando la popularidad
    let popularidad=document.createElement("h4")
    popularidad.classList.add("text-danger")
    popularidad.textContent=producto.popularidad

    //definiendo padres e hijos
    tarjeta.appendChild(imagen)
    tarjeta.appendChild(nombre)
    columna.appendChild(tarjeta)
    tarjeta.appendChild(precio)
    tarjeta.appendChild(descripcion)
    fila.appendChild(columna)
    tarjeta.appendChild(popularidad)


})